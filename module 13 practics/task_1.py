# Что нужно сделать
# Задано положительное число x (x > 0). Ваша задача — преобразовать его в формат плавающей точки, то есть ‌
# ‌, где 1 ≤ a < 10. Обратите внимание, что x теперь больше нуля, а не больше единицы. Обеспечьте контроль ввода.

# Пример 1

# Введите число: 92345

# Формат плавающей точки: x = 9.2345 * 10 ** 4

# Пример 2

# Введите число: 0.0012

# Формат плавающей точки: x = 1.2 * 10 ** -3

# Функция для проверки ввода
def pos_number():
    while True:
        try:
            x = float(input("Введите число: "))
            if x > 0:
                return x
            else:
                print("Ошибка: число должно быть больше нуля. Попробуйте еще раз.")
        except ValueError:
            print("Ошибка: введите корректное число.")

x = pos_number()

import math

# Определяем порядок
exponent = int(math.floor(math.log10(x)))
# Определяем мантиссу
mantissa = x / (10 ** exponent)

# Выводим в нужном формате
print(f"Формат плавающей точки: x = {mantissa} * 10 ** {exponent}")