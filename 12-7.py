# Задача «Наибольший общий делитель»
# Наибольший общий делитель (НОД) — это самое большое число, на которое можно разделить два или более числа без остатка.

# Например, для чисел 12 и 15 это число 3, потому что и 12, и 15 можно разделить на 3 без остатка, но большее число, которое делится на оба числа, не найти.

# Что нужно сделать
# Напишите функцию, вычисляющую наибольший общий делитель двух чисел.

# Пример работы программы:

# Введите первое число: 30
# Введите второе число: 18
# Наибольший общий делитель: 6

def gcd(a, b):
    """Вычисляет наибольший общий делитель (НОД) двух чисел a и b с помощью алгоритма Евклида."""
    while b != 0:
        a, b = b, a % b
    return a

# Ввод чисел от пользователя
num1 = int(input("Введите первое число: "))
num2 = int(input("Введите второе число: "))

# Вычисление и вывод результата
result = gcd(num1, num2)
print(f"Наибольший общий делитель: {result}")